    private void seleccionarArchivoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seleccionarArchivoButtonActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.showOpenDialog(null);
        File archivo = fileChooser.getSelectedFile();
        String nombreArchivo = archivo.getAbsolutePath();
        rutaTextField.setText(nombreArchivo);
    }//GEN-LAST:event_seleccionarArchivoButtonActionPerformed

    private void enviarButtonActionPerformed(java.awt.event.ActionEvent evt){//GEN-FIRST:event_enviarButtonActionPerformed
        // Codigo para recuperar el mensaje y enviarlo.
        String mensaje = mensajeTextField.getText();
        // Enviamos el texto del mensaje al socket.
        if(!mensaje.equals("")){
            clienteUDPMensajes.leerMensaje(mensaje);
            mensajesTextArea.append("Yo: " + mensaje + "\n");
            mensajeTextField.setText("");
        }
    }//GEN-LAST:event_enviarButtonActionPerformed

    private void enviarArchivoButtonActionPerformed(java.awt.event.ActionEvent evt){//GEN-FIRST:event_enviarArchivoButtonActionPerformed
        if(!rutaTextField.getText().equals("")){
            File archivo = new File(rutaTextField.getText());
            try{
                clienteTCP = new ClienteTCP(SERVER,50001);
                clienteTCP.setLabels(tasaLabel,totalLabel,restanteLabel,transcurridoLabel);
                clienteTCP.leerArchivo(archivo);
                clienteTCP.inicia();
            }catch (Exception e) {
                System.err.println(e.getMessage());
                System.exit(1);
            }
        }
    }//GEN-LAST:event_enviarArchivoButtonActionPerformed

    private void iniciarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_iniciarButtonActionPerformed
        try{
            webCam = new WebcamClientUDP(SERVER, 50003, videoLabel);
            webCam.inicia();
            audio = new AudioClienteUDP(SERVER);
            audio.iniciar();
        } catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_iniciarButtonActionPerformed

    private void detenerButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_detenerButton1ActionPerformed
        webCam.detener();
        audio.detener();
        clienteUDPMensajes.leerMensaje("Finaliza Transmision");

    }//GEN-LAST:event_detenerButton1ActionPerformed

    private void cancelButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelButtonActionPerformed
        if(clienteTCP != null){
            clienteTCP.cancelar();
        }
    }//GEN-LAST:event_cancelButtonActionPerformed

